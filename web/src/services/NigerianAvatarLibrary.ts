// Helper functions
export const getAvatarsByCategory = (category: string): DetailedAvatar[] => {
  if (category === 'all') return NIGERIAN_AVATAR_LIBRARY;
  return NIGERIAN_AVATAR_LIBRARY.filter(a => a.category === category);
};

export const getAvatarsByEthnicity = (ethnicity: string): DetailedAvatar[] => {
  if (ethnicity === 'all') return NIGERIAN_AVATAR_LIBRARY;
  return NIGERIAN_AVATAR_LIBRARY.filter(a => a.ethnicity === ethnicity);
};

export const getAvatarsByGender = (gender: 'male' | 'female'): DetailedAvatar[] => {
  return NIGERIAN_AVATAR_LIBRARY.filter(a => a.gender === gender);
};

export const searchAvatars = (query: string): DetailedAvatar[] => {
  const lowerQuery = query.toLowerCase();
  return NIGERIAN_AVATAR_LIBRARY.filter(a => 
    a.name.toLowerCase().includes(lowerQuery) ||
    a.profession.toLowerCase().includes(lowerQuery) ||
    a.description.toLowerCase().includes(lowerQuery) ||
    a.ethnicity.toLowerCase().includes(lowerQuery)
  );
};

